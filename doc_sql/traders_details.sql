CREATE TABLE `traders_details` (
	`userid` INT(5) NOT NULL,
	`cabang` VARCHAR(3) NULL DEFAULT NULL,
	`username` VARCHAR(16) NULL DEFAULT NULL,
	`address1` VARCHAR(100) NULL DEFAULT NULL,
	`address2` VARCHAR(100) NULL DEFAULT NULL,
	`city` VARCHAR(20) NULL DEFAULT NULL,
	`district` VARCHAR(20) NULL DEFAULT NULL,
	`province` VARCHAR(20) NULL DEFAULT NULL,
	`zipcode` INT(10) UNSIGNED NULL DEFAULT NULL,
	`avatar` VARCHAR(100) NULL DEFAULT NULL,
	`aff` TINYINT(4) UNSIGNED NOT NULL DEFAULT '0' COMMENT '1=aff, 2=ib',
	`telegram` VARCHAR(50) NULL DEFAULT NULL,
	`bigavatar` VARCHAR(100) NULL DEFAULT NULL,
	`thumb` VARCHAR(100) NULL DEFAULT NULL,
	`getprotect` TINYINT(3) UNSIGNED NOT NULL DEFAULT '0',
	`notes` TEXT NULL DEFAULT NULL,
	`token` VARCHAR(20) NULL DEFAULT NULL,
	`MODIFY_TIME` BIGINT(20) UNSIGNED NOT NULL DEFAULT '0',
	PRIMARY KEY (`userid`),
	UNIQUE INDEX `token` (`token`),
	UNIQUE INDEX `username` (`username`),
	INDEX `cabang` (`cabang`)
);